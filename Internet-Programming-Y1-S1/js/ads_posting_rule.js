const rule_list_container = document.getElementById("rule-list");
rule_data.forEach((data) => {
  title = document.createElement("h2");
  title.className = "tt";
  title.id = data.id;
  title.textContent = data.title;
  rule_list_container.appendChild(title);
  if (data.description != "") {
    description = document.createElement("p");
    description.text = data.description;
    rule_list_container.appendChild(description);
  }
  ol_element = document.createElement("ol");
  data.rule.forEach((rule) => {
    li_element = document.createElement("li");
    li_element.textContent = rule;
    ol_element.appendChild(li_element);
  });
  rule_list_container.appendChild(ol_element);
});

